package com.esgi.livraisonfinal.domain;

public class Livreur {
    private String id;
    private String nom;
    private String prenom;
    private String email;
    private String telephone;

    public Livreur(String id, String nom, String prenom, String email, String telephone) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.telephone = telephone;
    }


}
